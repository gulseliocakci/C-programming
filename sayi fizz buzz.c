#include<stdio.h>
/*girilen sayiya kadar olan sayilari yazar. 
3'un kati olanlarin yerine fizz, 
5'in kati olanlarin yerine buzz,
hem 3 hem 5'in kati olanlara fizz buzz yazar.*/
int main(void){
	int N, i;
	printf("bir N sayisi giriniz.\n");
	scanf("%d", &N);
	
	for(i=1; i<=N; i++){
		if((i%3==0)&&(i%5==0))
			printf("fizz buzz\n");
		else if(i%3==0)
			printf("fizz\n");
		else if(i%5==0)
			printf("buzz\n");
		else
		printf("%d \n", i);	
	}
	
	return(0);
}
